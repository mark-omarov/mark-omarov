export function getPaginatedCertificates(
  page = 1,
  pageSize = 9,
  filterCategory = 'all'
): {
  certificates: Certificate[];
  pagination: {
    total: number;
    totalPages: number;
    currentPage: number;
  };
} {
  const filteredAndSorted = certificates
    .filter((cert) => {
      if (filterCategory === 'all') return true;
      return cert.category === filterCategory;
    })
    .toSorted((a, b) => b.date.getTime() - a.date.getTime());

  const total = filteredAndSorted.length;
  const totalPages = Math.ceil(total / pageSize);
  const normalizedPage = Math.max(1, Math.min(page, totalPages || 1));

  const start = (normalizedPage - 1) * pageSize;
  const paginatedCerts = filteredAndSorted.slice(start, start + pageSize);

  return {
    certificates: paginatedCerts,
    pagination: {
      total,
      totalPages,
      currentPage: normalizedPage,
    },
  };
}
